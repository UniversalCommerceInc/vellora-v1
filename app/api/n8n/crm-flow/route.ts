import { NextRequest, NextResponse } from "next/server";

export async function POST(request: NextRequest) {
  try {
    const body = await request.json();
    const { platform, code } = body;
    console.log("Received request body:", body);

    if (!platform || !code) {
      return NextResponse.json(
        { error: "Platform and code are required" },
        { status: 400 }
      );
    }

    // Configure N8N endpoint based on platform
    let n8nEndpoint = "";

    switch (platform.toLowerCase()) {
      case "pipedrive":
        n8nEndpoint = `${process.env.N8N_PIPEDRIVE_WEBHOOK_URL}/import` || "";
        break;

      case "zoho":
        n8nEndpoint = `${process.env.N8N_ZOHO_WEBHOOK_URL}/import` || "";
        break;

      case "hubspot":
        n8nEndpoint = `${process.env.N8N_HUBSPOT_WEBHOOK_URL}/import` || "";
        break;

      default:
        return NextResponse.json(
          { error: `Unsupported platform: ${platform}` },
          { status: 400 }
        );
    }

    if (!n8nEndpoint) {
      return NextResponse.json(
        { error: `N8N endpoint not configured for ${platform}` },
        { status: 500 }
      );
    }

    console.log(`Calling N8N webhook for ${platform}...`);
    console.log(`Endpoint: ${n8nEndpoint}`);
    console.log(`Code: ${code}`);

    // Call N8N webhook
    const n8nResponse = await fetch(n8nEndpoint, {
      method: "GET",
      headers: {
        code: code,
        "Content-Type": "application/json",
      },
    });

    if (!n8nResponse.ok) {
      const errorText = await n8nResponse.text();
      console.error(`N8N webhook failed: ${n8nResponse.status} - ${errorText}`);
      return NextResponse.json(
        { error: `N8N webhook failed: ${n8nResponse.statusText}` },
        { status: n8nResponse.status }
      );
    }

    const n8nData = await n8nResponse.json();
    console.log(`N8N webhook successful for ${platform}`);
    console.log("N8N Response:", n8nData);

    // Return the data in the expected format
    return NextResponse.json({
      success: true,
      data: n8nData.data || n8nData, // Adjust based on your N8N response structure
      platform: platform,
    });
  } catch (error) {
    console.error("N8N API route error:", error);
    return NextResponse.json(
      {
        error: "Internal server error",
        details: error instanceof Error ? error.message : "Unknown error",
      },
      { status: 500 }
    );
  }
}

